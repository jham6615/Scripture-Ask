// Fetches the public-domain World English Bible (all 66 books) from getbible.net v2 and writes each
// book as JSON in the app's Book shape, plus a generated _books.ts that statically imports them all.
// Run:  node scripts/build-web-bible.mjs
import { mkdir, writeFile, rm } from 'node:fs/promises';
import path from 'node:path';

// [getbible book number, USFM id, display fallback]
const BOOKS = [
  [1, 'GEN'], [2, 'EXO'], [3, 'LEV'], [4, 'NUM'], [5, 'DEU'], [6, 'JOS'], [7, 'JDG'], [8, 'RUT'],
  [9, '1SA'], [10, '2SA'], [11, '1KI'], [12, '2KI'], [13, '1CH'], [14, '2CH'], [15, 'EZR'],
  [16, 'NEH'], [17, 'EST'], [18, 'JOB'], [19, 'PSA'], [20, 'PRO'], [21, 'ECC'], [22, 'SNG'],
  [23, 'ISA'], [24, 'JER'], [25, 'LAM'], [26, 'EZK'], [27, 'DAN'], [28, 'HOS'], [29, 'JOL'],
  [30, 'AMO'], [31, 'OBA'], [32, 'JON'], [33, 'MIC'], [34, 'NAM'], [35, 'HAB'], [36, 'ZEP'],
  [37, 'HAG'], [38, 'ZEC'], [39, 'MAL'], [40, 'MAT'], [41, 'MRK'], [42, 'LUK'], [43, 'JHN'],
  [44, 'ACT'], [45, 'ROM'], [46, '1CO'], [47, '2CO'], [48, 'GAL'], [49, 'EPH'], [50, 'PHP'],
  [51, 'COL'], [52, '1TH'], [53, '2TH'], [54, '1TI'], [55, '2TI'], [56, 'TIT'], [57, 'PHM'],
  [58, 'HEB'], [59, 'JAS'], [60, '1PE'], [61, '2PE'], [62, '1JN'], [63, '2JN'], [64, '3JN'],
  [65, 'JUD'], [66, 'REV'],
];

const OUT = path.resolve('src/lib/bible/data/web');

async function fetchBook(nr) {
  for (let attempt = 1; attempt <= 3; attempt++) {
    try {
      const res = await fetch(`https://api.getbible.net/v2/web/${nr}.json`);
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      return await res.json();
    } catch (e) {
      if (attempt === 3) throw e;
      await new Promise((r) => setTimeout(r, 500 * attempt));
    }
  }
}

async function main() {
  await mkdir(OUT, { recursive: true });
  const imports = [];
  const vars = [];

  for (const [nr, id] of BOOKS) {
    const raw = await fetchBook(nr);
    const book = {
      id,
      name: raw.name,
      translationId: 'web',
      chapters: raw.chapters.map((c) => ({
        chapter: c.chapter,
        verses: c.verses.map((v) => ({ verse: v.verse, text: String(v.text).trim() })),
      })),
    };
    const file = `${id.toLowerCase()}.json`;
    await writeFile(path.join(OUT, file), JSON.stringify(book));
    const v = `b${id}`;
    imports.push(`import ${v} from './${file}';`);
    vars.push(v);
    console.log(`${id}  ${book.name}  (${book.chapters.length} chapters)`);
  }

  const ts = `// AUTO-GENERATED by scripts/build-web-bible.mjs — do not edit by hand.\nimport type { Book } from '../../types';\n${imports.join('\n')}\n\nexport const WEB_BOOKS = [${vars.join(', ')}] as unknown as Book[];\n`;
  await writeFile(path.join(OUT, '_books.ts'), ts);

  // Remove the old single-book file (superseded by jhn.json).
  await rm(path.join(OUT, 'john.json'), { force: true });

  console.log(`\nWrote ${vars.length} books + _books.ts`);
}

main().catch((e) => {
  console.error('FAILED:', e);
  process.exit(1);
});
